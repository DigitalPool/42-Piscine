#include <stdio.h>
#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 != '\0' || *s2 != '\0')
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (0);
}

int	main()
{
	char *a = "Abdjsjjd";
	char *b = "ABX";
	printf("%d\n", ft_strcmp(a, b));
}
